package com.ecom.caddie;

public class Caddie {
	
	private int numPro;
	private String nomPro ;
	private int prixPro ;
	private int qtPro;
	
	public int getNumPro() {
		return numPro;
	}
	public void setNumPro(int numPro) {
		this.numPro = numPro;
	}
	public int getPrixPro() {
		return prixPro;
	}
	public void setPrixPro(int prixPro) {
		this.prixPro = prixPro;
	}
	public int getQtPro() {
		return qtPro;
	}
	public void setQtPro(int qtPro) {
		this.qtPro = qtPro;
	}
	public String getNomPro() {
		return nomPro;
	}
	public void setNomPro(String nomPro) {
		this.nomPro = nomPro;
	}
	
	
	
}
